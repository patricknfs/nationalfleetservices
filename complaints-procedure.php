<h2 class="entry-title">Complaints Procedure</h2>

<div class="entry-content">







<h4>Telling us you are unhappy with our service</h4>

<p>We aim to provide you with the highest standards of service.  However, there may be occasions when customers feel they have cause for complaint.  By letting us know you're unhappy with the service you've received, we have the opportunity to put things right.  Your feedback is also vital in helping us to improve our service.

</p><h4>

How to make a complaint? 

</h4><p>

If you would like to make a complaint you can contact us in a number of ways:

</p><p>

<b>By Phone:</b> 0121 45 45 645

</p><p>

<b>Email us:</b> <a href="mailto:info@nationalfleetservices.net">info@nationalfleetservices.net</a>

</p><p>

<b>Write to us:</b><br>
National Fleet Services Limited <br>
5 The Croft<br>
Buntsford Drive<br>
Bromsgrove<br>
B60 4JE<br>

</p><h4>

What happens next? 

</h4><p>
We will attempt to resolve your complaint as fairly and as promptly as possible. One of our Directors will investigate your complaint quickly but to ensure we reach a fair outcome for you, we want to conduct a thorough investigation and this can take time. Our Director will assess the details of your complaint thoroughly, fairly and impartially to reach a decision. To help us to do this, please make sure you have provided us with the following information:</p>

<ul>
<li>
Your name, address and telephone number 
</li><li>
Your registration number, account number or other reference number
</li><li>
A clear description of your concern or complaint 
</li><li>
Details of what you would like us to do to put things right 
</li><li>
Any supporting evidence or documentation
</li>
</ul>
<br>
<p>We aim to resolve the majority of complaints over the telephone.</p>
<p>
Where we believe that the complaint is not related to us, but is answerable by another party, we will forward your complaint to that party and inform you of our actions in writing.
</p>


<h4>How will I be kept updated? </h4>

<P>Our aim is to reach a conclusion within 4 weeks of the receipt of your complaint, but this can depend on the complexity of your case. If we haven't resolved your complaint by this time we'll ensure you are kept updated on the progress of our investigations. In the event that your complaint has not been resolved within 8 weeks from the date of receipt, we will provide you with a further update on the progress of our investigation, explaining why we are not yet in a position to provide you with our decision.

</p><h4>
How will I know the decision? 
</h4><p>

Whenever possible we'll always try to inform you of our decision over the telephone. In cases where we have not resolved your complaint by the end of the next business day, we'll also confirm our decision in writing to you in a final response letter. This letter will detail a full account of our findings and the rationale for our decision.
</p><h4>
What is The Financial Ombudsman Service? 
</h4><p>
Our aim is to resolve your complaint directly with you, but if you aren't happy with our final response, or 8 weeks have passed since you initially raised your complaint with us, you may have the right to refer your complaint to the Financial Ombudsman Service. The Financial Ombudsman Service was set up by law to give most consumers a free, independent service for resolving disputes with financial firms.
</p><p>
You have a period of 6 months to refer your complaint to the Financial Ombudsman Service. The 6 months starts from the date of the final response letter or 8 weeks after we received your complaint.
</p><p>
Further details on the service provided by the Financial Ombudsman Service are included in the booklet entitled "Your complaint and the Ombudsman" which we will provide to you at the appropriate point. The contact details for the Financial Ombudsman Service are:
</p><p>
The Financial Ombudsman Service <br>
Exchange Tower<br>
London<br>
E14 9SR </p>

<p><b>Telephone:</b> 0300 123 9123 or 0800 023 4567
</p><p>
<b>Fax:</b> 020 7964 1001
</p><p>
<b>Email:</b> <a href="mailto:complaint.info@financial-ombudsman.org.uk">complaint.info@financial-ombudsman.org.uk</a>
</p><p>
If your complaint is in respect of any other issue other than a consumer contract, we are members of the BVRLA (British Vehicle Rental Leasing Association)   Membership number is 1463.  If after our final response, you are not satisfied, then we will provide you with details of how to complain through them. 
</p>







