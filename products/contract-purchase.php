<h3>Contract Purchase (CP) is a type of finance agreement for business customers looking to fund a new vehicle in a manageable way</h3>

<p>Contract Purchase (CP) is ideal for any business that would like options at the end of its finance agreement. CP customers make an initial payment when they first take out the contract, then pay fixed monthly payments and finally have an Optional Final Payment (OFP) at the end at the end of the contract which is also referred to as the GFV (Guaranteed Future Value). You can trade-in your vehicle at a dealership and take another vehicle from them. If the trade-in value is larger than the OFP you will be able to use the difference towards a deposit on a new vehicle.  Or, you can simply return the vehicle to the funder, as long as you have not exceeded the mileage and the vehicle is in an appropriate condition for its age there will be no charge.  Finally, you can keep the vehicle either by paying the OFP in full or by re-financing it. </p>

<h4>Advantages:</h4>

<ul class="product-list">
<li>Low initial payment</li>
<li>Fixed monthly payments</li>
<li>You may be able to refinance the OFP</li>
<li>No depreciation concerns if you wish to walk away at the end</li>
<li>Maintenance and servicing can be included</li>
<li>Fixed OFP when you first take the contract out</li>
<li>Cost effective</li>
</ul>

<br />
<h4>Disadvantages:</h4>

<ul class="product-list">
<li>At the end of the contract you will have to decide whether you wish to sell the vehicle, return it or keep it</li>
<li>You must have fully comprehensive vehicle insurance</li>
<li>If you decide to return the vehicle at the end of the contract, it must be returned in a well maintained condition otherwise you will be charged for any damage over and above that stated in the ‘Fair Wear and Tear Guide’</li>
<li>You will be charged for all mileage in excess of the contract mileage (pro rata), should you choose to return the vehicle at the end of the contract</li>
</ul>

<br />
