<h3>A simple and cost effective way to fund any number of vehicles</h3>

<p>Contract Hire, sometimes referred to as an Operating Lease, is a long term rental agreement. Contracts range from 12 to 60 months and are tailored to the businesses requirements.  The Contract Hire Company reclaims the VAT on the original purchase, which reduces your monthly rentals (which are + VAT).  Contract Hire is a very popular choice for VAT registered companies as they can claim back 50% of the VAT on the finance element for cars and generally 100% for commercials (subject to no private use, no exempt turnover and not being on the Flat Rate VAT Scheme). On contracts with maintenance the VAT on the service element is 100% recoverable. One of the major benefits is that there are no disposal worries as the future value is underwritten by the leasing company.  Another benefit of Contract Hire is that it is generally ‘off balance sheet funding’ (subject to legislation change) which means it can improve your gearing ratio (assets to borrowing ratio) and therefore possibly your borrowing ability in the future.</p>

<h4>Advantages:</h4>


<ul class="product-list">
<li>Minimum capital expenditure</li>
<li>Accurate monthly budgeting</li>
<li>Improved cash flow</li>
<li>Fixed interest rates</li>
<li>Rentals can be offset against the businesses profits.  Cars with a CO2 output above 130g/km are currently subject to a 15% disallowance on the amount of the rental that can be claimed against the businesses taxation, for contract-hired cars with a CO2 output of 130g/km or below, there is currently no dis-allowance.</li>
<li>No vehicle disposal problems</li>
<li>Reduced administration</li>
<li>On-going advice and support</li>
<li>Road Fund Licence provided (vehicle excise duty paid) for duration of contract</li>
<li>Optional maintenance package</li>
<li>Optional breakdown and recovery</li>
<li>Optional replacement vehicle cover in event of a major breakdown</li>
<li>Optional GAP insurance which provides cover for the shortfall between the outstanding finance and the insurance value if the vehicle is declared a write-off by your insurance company, or is stolen and not recovered.</li>
</ul>


<br />
<h4>Disadvantages:</h4>

<ul class="product-list">

<li>Early termination can be expensive</li>
<li>If you do more miles than stated in you contract you will be charged excess mileage for each mile over that stated in your contract</li>
<li>You must look after the vehicle and return it in a well maintained condition otherwise you will be charged for any damage over and above that stated in the ‘Fair Wear and Tear Guide’</li>
<li>You must have fully comprehensive vehicle insurance</li>
<li>You will never own the vehicle as there is no option to buy it</li>
</ul>

