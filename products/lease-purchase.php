<h3>Lease Purchase is for people who would like to own a vehicle but do not necessarily have the money to buy one immediately</h3>

<p>Lease Purchase is for people who would like to own a vehicle but do not necessarily have the money to buy one immediately. It is ideal for non VAT registered customers who eventually wish to take ownership. It is a flexible product and it is possible to put down a larger initial payment, which in turn reduces your monthly payments. The monthly cost is worked out on the difference between the retail value and the depreciation value plus interest. The main difference between Lease Purchase and Contract Purchase is that instead of having the choice at the end of the contract to purchase the vehicle, which you would have with Contract Purchase, you have already entered into a contract to purchase the vehicle at the end of the contract with Lease Purchase. This contract is only for those who are absolutely sure that they want to take ownership of the vehicle at the end of the contractual period, and pay any balloon payments associated with the contract. Lease Purchase agreements typically run between 24 and 60 months, although the agreement can be settled at any time.</p>

<h4>Advantages:</h4>

<ul class="product-list">
<li>Low initial payment</li>
<li>Fixed mileage contract</li>
<li>Ideal for non-VAT registered customers who want eventual ownership of the vehicle</li>
<li>Effective budgeting with balloon facility, ownership of the vehicle is acquired once the balloon has been paid</li>
<li>Monthly payments are not subject to VAT</li>
<li>The vehicle is a company asset for balance sheet purposes</li>
<li>Lease Purchase frees up your capital for other business uses</li>
</ul>

<br />
<h4>Disadvantages:</h4>

<ul class="product-list">
<li>The balloon payment must be paid for at the end of the contract</li>
<li>The vehicle is yours once you have paid the balloon payment. In some cases the balloon can be higher than its value at the end of the contract</li>
<li>You must have fully comprehensive vehicle insurance</li>
</ul>

<br />

<h4>More Information on Lease Purchase:</h4>

<p>It is a flexible product and it is possible to put down a larger initial payment, which has the advantage of reducing the monthly payments. The monthly cost is worked out on the difference between the retail value and the depreciation value plus interest. This means that choosing Lease Purchase for a vehicle which holds its value will work out in your favour. </p>
<p>The main difference between Lease Purchase and Contract Purchase is that instead of having the choice at the end of the contract to purchase the vehicle, which you would have with Contract Purchase, you have already entered into a contract to purchase the vehicle at the end of the contract with Lease Purchase.</p>
<p>This contract is only for those who are absolutely sure that they want to take ownership of the vehicle at the end of the contractual period, and pay any balloon payments attached to the contract. Lease Purchase agreements typically last between 2 and 4 years although the agreement can be settled at any time throughout the contract.</p>

