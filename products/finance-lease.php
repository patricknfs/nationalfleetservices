<h3>Finance Lease is a popular funding option for commercial vehicles where Contract Hire is not suitable. (Cars can be financed as well)</h3>

<p>Finance Lease is a method of financing a vehicle that is usually favoured for commercial vehicles and by VAT registered businesses.  The business obtains use of the vehicle by paying a rental each month.  The monthly rental is determined by the initial cost of the vehicle (excluding VAT), the period of the Finance Lease and the residual value (normally called the balloon payment), plus interest. Although you never take ownership, at the end of the Finance Lease contract a rental equivalent to the balloon payment is payable. Usually this means that the vehicle is sold and a proportion of the proceeds of the sale are returned to the lessee. 
Most Finance Lease companies will offer a number of payment options to suit your cash flow. You can lower the monthly rental with a balloon payment at the end of the contract, or you can pay the entire cost in monthly rentals (normally referred to as a fully amortised Finance Lease), in which case you may be able to extend the Finance Lease with a secondary period rental (sometimes called a peppercorn rental). </p>

<h4>Advantages:</h4>

<ul class="product-list">
<li>Minimum capital expenditure</li>
<li>Accurate monthly budgeting</li>
<li>A fixed interest rate is available on some contracts</li>
<li>No damage recharge as you are responsible for disposal of the vehicle</li>
<li>Finance lease is a popular choice for VAT registered companies as they can claim back 50% of the VAT on the finance element for cars and generally 100% for commercials (subject to no private use). On contracts with maintenance the VAT on the service element is 100% recoverable</li>
<li>Rentals can be offset against the businesses profits.  Cars with a CO2 output above 130g/km are currently subject to a 15% disallowance on the amount of the rental that can be claimed against the businesses taxation, for cars with a CO2 output of 130g/km or below, there is currently no disallowance</li>
<li>Optional maintenance package</li>
<li>Optional breakdown and recovery</li>
<li>Optional replacement vehicle cover in event of a major breakdown</li>
<li>Optional GAP insurance which provides cover for the shortfall between the outstanding finance and the insurance value if the vehicle is declared a write-off by your insurance company, or is stolen and not recovered</li>
</ul>

<br />
<h4>Disadvantages:</h4>

<ul class="product-list">
<li>You will never own the vehicle as you must sell it to a third party at the end of the agreement</li>
<li>Operating risk associated with running the vehicle</li>
<li>Interest rates can vary on some contracts</li>
<li>You must have fully comprehensive vehicle insurance</li>
</ul>

<br />
