<h3>As the name suggests Personal Contract Hire (also known as personal leasing) is simply contract hire but for private individuals</h3>

<p>Personal Contract Hire (PCH) is based on a fixed term contract where customers pay an agreed monthly rental for the use of a vehicle for a set period. It benefits customers wishing to eliminate the financial risk associated with disposal of a vehicle. Contracts are usually taken over two, three or four years, providing a high level of flexibility.  Some contracts can be formally extended beyond the end of contract date if required. PCH is very similar to business Contract Hire; both are based on a fixed annual mileage agreed on inception of the contract. You will need to work out how many miles you drive each year before a quote can be obtained. If you do exceed the agreed mileage at the end of your contract you will pay an excess mileage charge, which will have been agreed upon prior to signing your finance agreement. If you want fixed motoring costs consider adding maintenance to your contract.  Maintenance generally covers servicing, routine maintenance, tyres, exhausts and batteries etc.  It is important that the vehicle is returned in accordance with the guidelines set out in the ‘Fair Wear and Tear Guide’, a copy of which is made available to customers at the beginning of their PCH agreement.</p>

<h4>Advantages:</h4>

<ul class="product-list">
<li>Flexible initial rental</li>
<li>Fixed term contract</li>
<li>Flexible term and mileage to suit your requirements</li>
<li>You only pay for the use of the vehicle</li>
<li>At the end of your contract simply hand the vehicle back</li>
<li>Option of including maintenance with the contract</li>
<li>No depreciation or disposal risk</li>
</ul>

<br />
<h4>Disadvantages:</h4>

<ul class="product-list">
<li>Early termination can be expensive</li>
<li>If you do more miles than stated in your contract you will be charged excess mileage for each mile over that stated in your contract</li>
<li>You must look after the vehicle and return it in a well maintained condition otherwise you will be charged for any damage over and above that stated in the ‘Fair Wear and Tear Guide’</li>
<li>You must have fully comprehensive vehicle insurance </li>
<li>You will never own the vehicle as there is no option to buy it</li>
</ul>

<br />
<h4>More Information on Personal Contract Hire:</h4>

<p>It benefits customers wishing to eliminate the financial risk associated with disposing of a vehicle. Contracts are usually taken over two, three or four years, providing a high level of flexibility.  Some contracts can be formally extended beyond the end of contract date if required. Just ask and we can answer any of your questions regarding extending your contract.</p>
<p>PCH is very similar to business Contract Hire; both are based on a fixed annual mileage agreed on inception of a contract. You will need to decide how many miles you drive each year before quotations can be obtained. Once you have stated how many miles you drive in a year and considered which vehicle you would like to lease a quote can be created for you by one of our sales team.</p>
<p>Regulations with regard to making mileage amendments after contracts are live vary from funder to funder, so it is always better to be as accurate as you can be. If you do exceed the agreed allowance at the end of your contract you will pay an excess mileage charge, which will have been agreed upon prior to signing the finance agreement.</p>
<p>Please remember this when deciding upon you annual mileage.  If you are looking to have predictable motoring costs adding maintenance to your monthly payment is easy. Maintenance is there to put your mind at rest should any challenges arise with your vehicle and generally covers servicing, routine maintenance, tyres, exhausts and batteries.</p>
<p>Please note that it is responsibility of the person named on the finance agreement to arrange and pay for fully comprehensive insurance for the vehicle.  It is important that the vehicle is returned in accordance with the guidelines set out in the ‘Fair Wear and Tear Guide’, a copy of which is made available to customers when they are near the end of their PCH agreement.</p>


