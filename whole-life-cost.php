<h3>Showing you the full cost of your lease vehicles</h3>

<p>Whole Life Cost (WLC) is a financial model to help show the true cost of running a vehicle, incorporating all costs rather than just monthly rentals. There are many elements to running a vehicle and by taking them all into consideration you are able to make better and more cost efficient choices for your fleet.</p>

<p>Whole life cost includes:</p>

<ul>
<li>Monthly rental - reflecting changes in Road Fund Licence and CO2 based Capital Allowances</li>
<li>Disallowable VAT - allowing your business to take advantage of your true VAT position</li>
<li>Fuel costs - in addition to options for fuel and diesel costs, we can customise fuel costs based on your pence per mile rate, as well as separating business and private mileage</li>
<li>Employers National Insurance Contribution</li>
<li>Vehicle insurance - costs can be included</li>
<li>Lease rental restriction - CO2 based allowances can be factored into WLC based on the Corporation Tax rate of your business</li>
<li>Fleet Administration fee - to help recognise the administration of your fleet within monthly rentals</li>
</ul>

