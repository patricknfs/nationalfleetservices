<h3>Car Leasing vs Buying</h3>
<p>There are many practical reasons to leasing a car than buying. Features include:</p>
<ul>
<li>Fixed, lower monthly rentals</li>
<li>Lower initial rental</li>
<li>Inclusive road tax and breakdown recovery</li>
<li>Variable contract duration and mileage terms</li>
<li>Optional <a href="http://www.nationalfleetservices.net/maintenance-servicing.php">maintenance package</a> to spread the cost of servicing</li>
</ul>
<br />


<h3>The Order Process</h3>

<h5>1. Your vehicle is ordered with one of our supplying dealers.</h5>
<h5>2. You will receive an email confirming when your vehicle is expected to arrive with our supplying dealer.</h5>
<h5>3. Finance documents are emailed to you by our administration team.</h5>
<h5>4. You will be required to sign and return the finance documents via post.</h5>
<h5>5. An email will be sent to you to confirm receipt of your signed documents.</h5>
<h5>6. Once your vehicle is with our supplier, we will be in touch to arrange a convenient time for delivery.</h5>
<h5>7. Upon delivery you will be required to sign a vehicle acceptance note.</h5>
<br />

<h3>What is leasing? / Leasing explained</h3>
<p>Leasing is a long-term rental agreement offering the exclusive use of a car or van for a set period of time at a fixed monthly price. It offers an alternative, and often cheaper, funding solution to buying a new car or van with a bank loan or dealer finance.</p>
<p>Car leasing helps you avoid any unexpected costs by offering a fixed monthly rental for the term of the lease. The customer also doesn’t have to worry about depreciation or selling the car as this job falls to the lease company.</p>

<h3>How Leasing Works</h3>
<p>At the start of a leasing contract, the customer pays an initial rental usually equivalent to three monthly rentals.</p>
<p>The customer then makes monthly rentals for the duration of the contract term. At the end of the lease contract (typically two, three years or four years), the customer simply hands the car back without any further obligations, or may choose to purchase the vehicle depending on the type of contract taken.</p>

<p>If there is any end of contract repair work required or you have exceeded your agreed mileage charges will apply</p>

<p>With a leasing contract, you also have the opportunity to drive a new car every two, three or four years and all of the vehicles include the standard manufacturer’s warranty?</p>

