<b>2. Whose products do we offer?
    </b></p><p>
We can only offer products from a limited number of lenders for vehicle finance.  Ask us for a list of lenders from whom we can offer finance.</p><p>
<b>3. Which service will we provide you with?</b></p><p>
You will not receive advice or a recommendation from us for vehicle finance or leasing.  We may ask some questions to narrow down the selection of products that we will provide details on.  You will then need to make your own choice about how to proceed.  We have attached the advantages and disadvantages of the product types available to you.  Eg. Personal Finance would be PCH (Personal Contract Hire) or PCP (Personal Contract Purchase)</p><p>
We are not an IFA (Independent Financial Adviser).  We may receive a remuneration from a lender we introduce you to.</p><p>
<b>4. What will you have to pay us for our services?</b></p><p>
For non-regulated customers (limited companies and partnerships with 4 or more partners), the following fees will be payable to National Fleet Services Ltd:
</p><p>£150.00 + Vat for Contract Hire & Lease purchase products. £125.00 + Vat will be payable for Finance Lease.  Any Purchase style agreements will have option to purchase fees as stated on the quotes.  </p><p>	
In addition to the fees charged by National Fleet Services, the lender may also charge their own fees.  These include but are not limited to: documentation fees, option to purchase fees, excess mileage charges or administration fees.</p><p>
For regulated customers (private individual, sole traders and partnerships with 3 or less partners) the folowing fees will be payable to National Fleet Services Ltd:
</p><p>£180.00 Inc Vat for personal contract hire and personal contract purchase. Any purchase style agreements will have 'option to purchase' fees as stated on the quotes. </><p>
In addition to the fees charged by National Fleet Services Ltd, the lender may also charge their own fees. These include but are not limited to: documentation fees, option to purchase fees, excess mileage charges or administration fees.</p><p>
